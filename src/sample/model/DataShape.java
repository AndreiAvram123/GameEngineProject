package sample.model;

public class DataShape {
    private String className;
    private double x;
    private double y;
    private double height;
    private double width;
    private String color;

    public DataShape(String className, double x, double y, double height, double width, String color) {
        this.className = className;
        this.x = x;
        this.y = y;
        this.height = height;
        this.width = width;
        this.color = color;
    }
}
